#define BUFSIZE 1024

int parse_output(char* cmd) {
    char buf[BUFSIZE];
    FILE *fp;

    if ((fp = popen(cmd, "r")) == NULL) {
        printf("Error opening pipe!\n");
        return -1;
    }

    while (fgets(buf, BUFSIZE, fp) != NULL) {
        // Do whatever you want here...
        printf("OUTPUT: %s", buf);
    }

    if(pclose(fp))  {
        printf("Command not found or exited with error status\n");
        return -1;
    }

    return 0;
}


int main(void) {
	puts("Hello World!");

	parse_output("ls -la");
	parse_output("/system/bin/input touchscreen swipe 300 300 500 1000 100");

	return 0;
}
